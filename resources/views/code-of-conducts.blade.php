         <h3>অভিভাবকদের জ্ঞাতব্যঃ</h3>
         <br>
         <p>১. যে সকল অভিভাবক/অভিভাবিকা তাদের সন্তানদের স্কুলে পৌঁছে দেন তারা স্কুলের গেটে তাদের পৌঁছে দিয়ে অনুগ্রহপূর্বক চলে যাবেন। কোন মহিলা অভিভাবক ছুটি পর্যন্ত অপেক্ষা করে তার সন্তানকে নিয়ে যেতে চাইলে তিনি স্কুলের নিচতলায় নির্ধারিত কক্ষে অবস্থান করবেন। কোন অবস্থাতেই কক্ষের বাইরে, আঙ্গিনায় যত্র তত্র বসবেন না বা ঘোরাফেরা করবেন না।
            <br>
         ২. আপনার পোষ্য/ছেলে/মেয়ে একদিন অনুপস্থিত থাকলে আপনার মন্তব্য ও স্বাক্ষরযুক্ত আবেদনপত্র ছুটি মঞ্জুরির জন্য তার মারফত পাঠাবেন। কোন ছাত্র/ছাত্রী কোন কারণে একদিনের বেশি অনুপস্থিত থাকলে অভিভাবক নিজে এসে ছুটি মঞ্জুর করিয়ে নিবেন।
         <br>
         ৩. কোন ছাত্র/ছাত্রী অসুস্থতার কারণে স্কুলে আসতে না পারলে অতি-সত্বর অভিভাবক একটি দরখাস্ত লিখে শ্রেণি শিক্ষকের কাছে জমা দিয়ে যাবেন এবং সুস্থ হলে মেডিকেল সার্টিফিকেটসহ অভিভাবক তাকে স্কুলে নিয়ে আসবেন এবং ছুটি মঞ্জুর করিয়ে নিবেন।
         <br>
         ৪. চলতি মাসের বেতন ৫,১০,১৫ ও ২০ তারিখের মধ্যে বিনা জরিমানায় পরিশোধ করা যাবে। ২০ তারিখের মধ্যে বেতন পরিশোধ না করলে জরিমানাসহ বেতন পরিশোধ করতে হবে।
         <br>
         ৫. কোন ছাত্র/ছাত্রী পরীক্ষায় কোন প্রকার অসদুপায় অবলম্বন করলে তাকে স্কুল থেকে বহিষ্কার করা হবে।
         <br>
         ৬. কোন ছাত্র/ছাত্রী ক্লাসে একবার ফেল করলে এবং সেই ক্লাসে আবার পড়তে চাইলে, পরবর্তী বছরের প্রথম কার্যদিবসে আবেদন করতে হবে এবং প্রথম বেতন আদায়ের তারিখে স্কুলের বেতন ও অন্যান্য পাওনা পরিশোধ করতে হবে। পুণঃভর্তি অবশ্যই তার সন্তোষজনক আচরণের উপর নির্ভর করবে।
         <br>
         ৭. কোন ছাত্র/ছাত্রী পরীক্ষায় ফেল করলে উপরের শ্রেণিতে তার প্রমোশনের ব্যাপারে কোন প্রকার তদবির চলবে না।
         <br>
